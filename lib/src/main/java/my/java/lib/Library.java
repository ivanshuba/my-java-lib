package my.java.lib;


/**
 * Library class providing various test methods for different data types.
 */
public class Library {
    /**
     * Test method that returns a boolean value.
     *
     * @return Always returns true
     */
    public boolean testBooleanLibraryMethod() {
        return true;
    }

    /**
     * Test method that returns a string value.
     *
     * @return The string "Hello World!"
     */
    public String testStringLibraryMethod() {
        return "Hello World!";
    }

    /**
     * Test method that returns an integer value.
     *
     * @return The integer value 1984
     */
    public int testIntLibraryMethod() {
        return 1984;
    }

    /**
     * Test method that returns a double value.
     *
     * @return The double value 1984.0
     */
    public double testDoubleLibraryMethod() {
        return 1984.0;
    }
}
